n = input()
print(n[2])     # третий символ строки
print(n[-2])    # предпоследний символ
print(n[:5])    # первые пять символов
print(n[:- 2])  # вся строка кроме послених двух
print(n[::2])   # чётные (начиная с нуля)
print(n[1::2])  # нечётные
print(n[::-1])  # обратный порядок
print(n[::-2])  # все через один с первого в обратном порядке
print(len(n))   # длина строки
print(n[0])


